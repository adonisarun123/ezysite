create table public.hire_helper_leads (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  name text not null,
  phone text not null,
  email text null,
  city text not null,
  service text not null,
  duration text null,
  start_date text not null,
  specific_requirements text null,
  experience text null,
  budget text null,
  languages text null,
  additional_services text null,
  family_size text not null,
  preferred_gender text null,
  status text null default 'new'::text,
  assigned_to text null,
  locality text null,
  apartment text null,
  service_timings text null,
  constraint hire_helper_leads_pkey primary key (id)
) TABLESPACE pg_default;